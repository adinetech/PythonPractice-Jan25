s = "Hello, World!"

# Reverse 
reversed_s = s[::-1]

# Skip every letter
skipped_s = s[::2]

# Extract a substring
substring = s[7:12]

# Get 5 characters
first_five = s[:5]

# Get last 5 characters
last_five = s[-5:]

print("Original string:", s)
print("Reversed string:", reversed_s)
print("Every other letter:", skipped_s)
print("Substring:", substring)
print("First 5 characters:", first_five)
print("Last 5 characters:", last_five)
